import { SelectField } from '../../components/SelectField';
import { SelectFieldMany } from '../../components/SelectFieldMany';
import { RichTextField } from '../../components/RichTextField';

import { create } from '../../stores/features/featuresSlice';
import { useAppDispatch } from '../../stores/hooks';
import { useRouter } from 'next/router';

const TablesPage = () => {
  const router = useRouter();
  const dispatch = useAppDispatch();

  const handleSubmit = async (data) => {
    await dispatch(create(data));
    await router.push('/features/features-list');
  };
  return (
    <>
      <Head>
        <title>{getPageTitle('New Item')}</title>
      </Head>
      <SectionMain>
        <SectionTitleLineWithButton
          icon={mdiChartTimelineVariant}
          title='New Item'
          main
        >
          Breadcrumbs
        </SectionTitleLineWithButton>
        <CardBox>
          <Formik
            initialValues={{
              feature_name: '',

              pdf_form: '',

              comparable_properties: [],
            }}
            onSubmit={(values) => handleSubmit(values)}
          >
            <Form>
              <FormField label='FeatureName'>
                <Field name='feature_name' placeholder='Your FeatureName' />
              </FormField>

              <FormField label='PDFForm' labelFor='pdf_form'>
                <Field
                  name='pdf_form'
                  id='pdf_form'
                  component={SelectField}
                  options={[]}
                  itemRef={'pdf_forms'}
                ></Field>
              </FormField>

              <FormField
                label='ComparableProperties'
                labelFor='comparable_properties'
              >
                <Field
                  name='comparable_properties'
                  id='comparable_properties'
                  itemRef={'features'}
                  options={[]}
                  component={SelectFieldMany}
                ></Field>
              </FormField>

              <BaseDivider />
              <BaseButtons>
                <BaseButton type='submit' color='info' label='Submit' />
                <BaseButton type='reset' color='info' outline label='Reset' />
                <BaseButton
                  type='reset'
                  color='danger'
                  outline
                  label='Cancel'
                  onClick={() => router.push('/features/features-list')}
                />
              </BaseButtons>
            </Form>
          </Formik>
        </CardBox>
      </SectionMain>
    </>
  );
};

TablesPage.getLayout = function getLayout(page: ReactElement) {
  return (
    <LayoutAuthenticated permission={'CREATE_FEATURES'}>
      {page}
    </LayoutAuthenticated>
  );
};

export default TablesPage;
